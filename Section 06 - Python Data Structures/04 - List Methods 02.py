fruits = ["Berries", "Apples", "Grapes", "Oranges"]
vegetables = ["Kale", "Broccoli", "Lettuce"]

# Method index
print(fruits.index('Grapes'))

# Method insert
fruits.insert(0, "Banana")
print(fruits)

# Method pop
fruits.pop()
print(fruits)

fruits.pop(1)
print(fruits)

# Method remove
vegetables.remove("Kale")
print(vegetables)
